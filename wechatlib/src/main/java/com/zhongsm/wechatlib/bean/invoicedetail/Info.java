package com.zhongsm.wechatlib.bean.invoicedetail;

public class Info {

    private String name;
    private int price;

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return this.name;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getPrice() {
        return this.price;
    }
}
